#some more delete methods in Lists

names=['Colt','Shelly','Spike','Poco','Bull','Crow']
names.remove('Shelly')      #removes the specified element
print(names)

nick=['Colt','Shelly','Spike','Poco','Bull','Crow']
nick.pop()                  #removes the last element in the List
print(nick)

code=['Colt','Shelly','Spike','Poco','Bull','Crow']
code.clear()                #removes the entire list elements
print(code)